print('Conversão de Celsius para Fahrenheit')
print('Temperatura Mínima em Celsius: -273,15ºC')
celsius = float(input('Qual a temperatura em Celsius? '))
fahrenheit = ((9*celsius)/5)+32
print(f'A temperatura de {celsius:.2f}ºC equivale a {fahrenheit:.2f}ºF')
